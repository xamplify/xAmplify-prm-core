package com.xtremand.team.member.group.exception;

public class TeamMemberGroupDataAccessException extends RuntimeException {


	public TeamMemberGroupDataAccessException() {
		super();
	}

	public TeamMemberGroupDataAccessException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TeamMemberGroupDataAccessException(String message, Throwable cause) {
		super(message, cause);
	}

	public TeamMemberGroupDataAccessException(String message) {
		super(message);
	}

	public TeamMemberGroupDataAccessException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = -8197057141551661459L;



}
